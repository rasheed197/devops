#!/usr/bin/bash

# Open Terminal --> CTRL+ALT+D

# The nano command launches the GNU nano text editor, a user-friendly, command-line based text editor.

# Open/Create file
# If filename exists, nano opens it. If filename does not exist, 
# nano creates and opens a new blank file with that name. 
nano filename

# Saving and Exiting:
# ----------------------------
# Save: Press Ctrl + O (Write Out), then press Enter to confirm the filename.
# Exit: Press Ctrl + X (Exit). If unsaved changes exist, nano prompts to save them.



